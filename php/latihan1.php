<?php
//PENGULANGAN

//for
for ($i = 0; $i < 5; $i++){
    echo "syntax for <br>";
}

// while
$i = 0;
while ($i <= 10) {
    echo "syntax while <br>";
    $i++;
}


// do..while
// untuk syntax do..while, ketika kondisinya bernilai false, perintahnya akan dijalankan dulu sekali
$i = 5;
do {
    echo "syntax do..while <br>";
    $i++; 
} while ($i <= 10);

?>
